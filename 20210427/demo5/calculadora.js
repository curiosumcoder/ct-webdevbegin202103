class Calculadora {
    Sumar(a,b) {
        console.log(`Llamando a Sumar: a = ${a}, b = ${b}`);
    
        return a + b;
    }
    
    Restar(a,b) {
        console.log(`Llamando a Restar: a = ${a}, b = ${b}`);
    
        return a - b;
    }
    
    Multiplicar(a,b) {
        console.log(`Llamando a Multiplicar: a = ${a}, b = ${b}`);
    
        return a * b;
    }
    
    Dividir(a,b) {
        console.log(`Llamando a Dividir: a = ${a}, b = ${b}`);
    
        return a / b;
    }
}